                        Muchas sociedades antiguas celebraban festivales en honor a sus dioses en la época del solsticio de invierno, el que coincide con la temporada de la Navidad
                    </p>
                    <p>
                        Estas celebraciones a menudo incluían el <strong>intercambio de regalos</strong>, por lo general de los patrones a sus trabajadores
                    </p>
                    <p>
                        Según las escrituras dentro de la Biblia, tres reyes magos (hombres sabios del este) fueron a visitar a Jesús recién nacido
                    </p>
                    <p>
                        Estos magos le llevaron regalos al niño
                    </p>
                    <p>
                        Hacer regalos en Navidad es reconocido por muchos como un recuerdo de este evento y un acto que tiene la intención de recordar a los creyentes el regalo que Dios les ha dado en la forma de su hijo, Jesucristo
                    </p>
                    <p>
                        En todo el mundo las sociedades fueron adoptando la tradición de obsequiar algo no solo a los niños sino también a los familiares y amigos
                    </p>
                    <p>
                        Ha sido también muy común colgar medias llenas de golosinas para disfrutarlas en la Nochebuena, mientras se entregan y reciben los obsequios
                    </p>
                    <p>
                        Los regalos de Navidad se han convertido en el aspecto más popular de estas fiestas
                    </p>
                    <p>
